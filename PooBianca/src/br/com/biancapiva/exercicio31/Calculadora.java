package br.com.biancapiva.exercicio31;

public class Calculadora {
    int soma;
    double somar;

    public Calculadora() {
    }
    public double somar(int a, int b) {
        return soma = a + b;
    }
    public double somar(double a, double b) {
        return somar = a + b;
    }
    public double somar(int a, int b, int c) {
        return soma = a + b + c;
    }
}
